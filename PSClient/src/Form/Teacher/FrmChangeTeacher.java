    private void schoolFieldItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_schoolFieldItemStateChanged
        try {
            chosenSchool = Communication.getInstance().getSchool(schoolList.get(schoolField.getSelectedIndex()));
        } catch (Exception ex) {
            java.util.logging.Logger.getLogger(FrmChangeTeacher.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
        addressField.setText(chosenSchool.getAddress());
        phoneField.setText(chosenSchool.getPhone());
    }//GEN-LAST:event_schoolFieldItemStateChanged

    private void changeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeButtonActionPerformed
        if(validateData().isEmpty()){
            teacherToChange.setFirstName(firstNameField.getText());
            teacherToChange.setLastName(lastNameField.getText());
            teacherToChange.setJmbg(jmbgField.getText());
            teacherToChange.setSchool(chosenSchool);
            try {
                Communication.getInstance().editTeacher(teacherToChange);
                JOptionPane.showMessageDialog(this, "Nastavnik je uspešno izmenjen.");
            } catch (Exception ex) {
                java.util.logging.Logger.getLogger(FrmChangeTeacher.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this,validateData());
        }
    }//GEN-LAST:event_changeButtonActionPerformed


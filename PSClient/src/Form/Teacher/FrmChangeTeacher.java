    private String validateData() {
        String errorMessage = "";
        System.out.println(firstNameField.getText() + "\n" + lastNameField.getText());
        
        if(firstNameField.getText().isEmpty()){
            errorMessage += "Ime ne sme biti prazno!\n";
        }
        if(lastNameField.getText().isEmpty()){
            errorMessage += "Prezime ne sme biti prazno!\n";
        }
        return errorMessage;
    }
    
    private void fillFields() {
        firstNameField.setText(teacherToChange.getFirstName());
        lastNameField.setText(teacherToChange.getLastName());
        jmbgField.setText(teacherToChange.getJmbg());
        schoolField.setSelectedItem(teacherToChange.getSchool().getName());
        addressField.setText(teacherToChange.getSchool().getAddress());
        phoneField.setText(teacherToChange.getSchool().getPhone());
    }

    private void disableFields() {
        firstNameField.setEditable(false);
        lastNameField.setEditable(false);
        jmbgField.setEditable(false);
        schoolField.setEnabled(false);
        addressField.setEditable(false);
        phoneField.setEditable(false);
    }

    private void initData() {
        try {
            schoolList = Communication.getInstance().getAllSchools();
            for(School school:schoolList)
                schoolField.addItem(school.getName());
        } catch (Exception ex) {
            java.util.logging.Logger.getLogger(FrmChangeTeacher.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

    private void enableFields() {
        firstNameField.setEditable(true);
        lastNameField.setEditable(true);
        jmbgField.setEditable(true);
        schoolField.setEnabled(true);
    }
